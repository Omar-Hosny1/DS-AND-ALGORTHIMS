/**
 *        9
 *      /   \
 *    4      20
 *   / \    /  \
 *  1   6  15   170
 *
 * in a list => [9, 4, 1, 6, 20, 15, 170]
 *
 * There are 3 ways to implement Depth First Search :
 * 1 - inorder
 * 2 - preorder
 * 3 - postorder
 *
 *    101
 *   /   \
 * 33     105
 *
 * inorder => [33, 101, 105]
 * preorder => [101, 33, 105]
 * postorder => [33, 105, 101]
 * */
